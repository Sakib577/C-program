#include <stdio.h>
#include <stdlib.h>
#include <math.h>

int main()
{

    printf("addition %f\n", 4.3 + 2);
    printf("division %f\n", 50.0 / 30.);
    printf("substraction %f\n", 4.3 - 2);
    printf("multiplication %d\n", 5 * 6);
    printf("power %f\n", pow(2, 4));
    printf("square root %f\n", sqrt(25));
    printf("ceiling %f\n", ceil(41.259));
    printf("flooring %f\n", floor(41.259));
    /*this is the tutorial of simple math formulas and comments in c programming language.*/

    return 0;
}
